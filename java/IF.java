public class IF {
    public static void main(String[] args) {
        /*
        语法：if(布尔表达式){
        执行代码1}
        else{
        执行代码2}
         */
        int a = 10;
        if(a>20){
            System.out.println("can");
        }else {
            System.out.println("can't");
        }
            int b = 30;
        if(b==10){//==的意思是相对值
            System.out.println("b=10");
        }else if(b==20){
            System.out.println("b=20");
        }else if(b==30){
            System.out.println("b=30");
            }else{
            System.out.println("else结束");
        }
        }
    }
